package com.example.transparencia.model;

import java.util.List;

public class AtributosFormato {
	
	private String nombreHoja;
	private List<String> listaFila;
	
	public String getNombreHoja() {
		return nombreHoja;
	}
	public void setNombreHoja(String nombreHoja) {
		this.nombreHoja = nombreHoja;
	}
	public List<String> getListaFila() {
		return listaFila;
	}
	public void setListaFila(List<String> listaFila) {
		this.listaFila = listaFila;
	}

}
